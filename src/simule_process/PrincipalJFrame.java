    private void nuevos_procesosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuevos_procesosActionPerformed
        if(nuevo_nombre.getText().length()!=0){
            if(!administrador_procesos.existe_proceso(nuevo_nombre.getText())){
                administrador_procesos.setInactivos_actual(administrador_procesos.pos_proceso_entrada(nuevo_nombre.getText()));
                panel_progreso.nuevo_proceso(administrador_procesos.buscar_proceso(nuevo_nombre.getText()));
                nuevo_nombre.setText("");
                return;
            }
            dialogos.nombre_repetido(nuevos_procesos);
        }
        
    }//GEN-LAST:event_nuevos_procesosActionPerformed

