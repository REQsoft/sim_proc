    private void generar_aleatoriosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generar_aleatoriosActionPerformed
        modelo_lista.clear();
        administrador_procesos.setRata_quantum((int) valorQ.getValue());
        for(int i=0;i<(int)cantidad_aleatorios.getValue();i++){  
            modelo_lista.addElement("Proceso "+i);
            administrador_procesos.agragar_proceso("Proceso "+i);
        }
        lista_procesos.setModel(modelo_lista);
    }//GEN-LAST:event_generar_aleatoriosActionPerformed

    private void eliminar_procesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminar_procesoActionPerformed
        if(lista_procesos.getSelectedIndex()!=-1){     
            administrador_procesos.eliminar_proceso(lista_procesos.getSelectedIndex());
            modelo_lista.removeElementAt(lista_procesos.getSelectedIndex());
            lista_procesos.setModel(modelo_lista);           
        }
    }//GEN-LAST:event_eliminar_procesoActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(administrador_procesos.getEstado_emulador().equals("iniciado")){
            administrador_procesos.reiniciar_emulador();
            administrador_procesos=new Admin_procesos(this,inactivos,preparados,en_ejecucion,suspendidos);
            panel_progreso.borrar_procesos();
            valorQ.setValue(2);
            cantidad_aleatorios.setValue(1);
            generar_aleatorios.setEnabled(true);
            agregar_unico.setEnabled(true);
            nuevos_procesos.setEnabled(false);
            iniciar_simulador.setEnabled(false);
            habilitar_p_p.setEnabled(false);
            ejecutar_p_p.setEnabled(false);
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void iniciar_simuladorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciar_simuladorActionPerformed
        if(administrador_procesos.getEstado_emulador().equals("no iniciado")){
            panel_progreso.borrar_procesos();
            administrador_procesos.iniciar_simulador();
            panel_progreso.procesos_iniciales(administrador_procesos.getLista_procesos());
            nuevos_procesos.setEnabled(true);
            habilitar_p_p.setEnabled(false);
            ejecutar_p_p.setEnabled(false);
            administrador_procesos.start();
            return;
        }  
        habilitar_p_p.setEnabled(false);
        ejecutar_p_p.setEnabled(false);
        administrador_procesos.start();
    }//GEN-LAST:event_iniciar_simuladorActionPerformed

