    private void agregar_unicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar_unicoActionPerformed
   
        if(nombre_proceso.getText().length()!=0){
            administrador_procesos.setRata_quantum((int) valorQ.getValue());
            if(administrador_procesos.agragar_proceso(nombre_proceso.getText())){
                modelo_lista.addElement(nombre_proceso.getText());  
                lista_procesos.setModel(modelo_lista);   
            }
            else{
                dialogos.nombre_repetido(agregar_unico);
            }
        }
    }//GEN-LAST:event_agregar_unicoActionPerformed

    private void carcgar_inactivosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_carcgar_inactivosActionPerformed
        if(modelo_lista.size()!=0){
            panel_progreso.procesos_iniciales(administrador_procesos.getLista_procesos());   
            administrador_procesos.cargar_simulador();
            modelo_lista.clear();
            lista_procesos.setModel(modelo_lista);
            agregar_unico.setEnabled(false);
            generar_aleatorios.setEnabled(false);
            iniciar_simulador.setEnabled(true);
            habilitar_p_p.setEnabled(true);
            return;
        }
        dialogos.model_vacio(carcgar_inactivos);
    }//GEN-LAST:event_carcgar_inactivosActionPerformed

